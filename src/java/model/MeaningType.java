package model;

/**
 *
 * @author hoang
 */
public class MeaningType {
    private String id, type;

    public MeaningType() {
    }

    public MeaningType(String id, String type) {
        this.id = id;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    
}
